package exercicios;

public class Exercicio01 {
    public static void main(String[] args) {
        System.out.print("15+7=");
        System.out.println(15+7);
        System.out.print("15-7=");
        System.out.println(15-7);
        System.out.print("15*7=");
        System.out.println(15*7);
        System.out.print("15/7=");
        System.out.println(15/7);
        System.out.print("15/7=");
        System.out.println(15.0/7); //casas decimais
        System.out.println("Resposta de 15.0/7:" + 2 + 3); //concatenação
        System.out.println("Resposta de 15.0/7:" + 2 * 3); //concatenação
        System.out.println("Resposta de 15.0/7:" + (2 + 3)); //conta
        

    }
}
